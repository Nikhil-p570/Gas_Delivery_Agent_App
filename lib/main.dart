class AuthGate extends StatelessWidget {
  const AuthGate({super.key});

  @override
  Widget build(BuildContext context) {
    return StreamBuilder(
      stream: null, // Will be handled in LoginScreen
      builder: (_, __) => const LoginScreen(),
    );
  }
}